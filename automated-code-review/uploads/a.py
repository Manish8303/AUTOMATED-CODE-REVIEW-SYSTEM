"""
This module provides a simple, clean function for adding two numbers.
It adheres to standard Python style guides (PEP 8) for indentation and documentation.
"""


def add_numbers(a: float | int, b: float | int) -> float | int:
    """
    Calculates and returns the sum of two numbers.

    Parameters:
    - a (float | int): The first number.
    - b (float | int): The second number.

    Returns:
    - float | int: The sum of a and b.
    """
    return a + b


